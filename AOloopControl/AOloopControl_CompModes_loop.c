errno_t AOloopControl_CompModes_loop(const char *ID_CM_name,
                                     const char *ID_WFSref_name,
                                     const char *ID_WFSim_name,
                                     const char *ID_WFSimtot_name,
                                     const char *ID_coeff_name)
{
    int      *GPUsetM;
    imageID   ID_CM;
    imageID   ID_WFSref;
    imageID   ID_coeff;
    uint32_t  GPUcnt;
    int       GPUstatus[100];
    int       status;
    uint32_t  NBmodes;
    uint32_t *sizearray;

    imageID  ID_WFSim;
    imageID  ID_WFSim_n;
    uint32_t wfsxsize, wfsysize;
    imageID  IDcoeff0;

    imageID ID_WFSimtot;
    double  totfluxave;
    imageID ID_coefft;

    double alpha = 0.1;
    char   imname[200];

    // LOG function start
    int  logfunc_level     = 0;
    int  logfunc_level_max = 1;
    char commentstring[200];
    sprintf(commentstring, "loop %ld", aoloopcontrol_var.LOOPNUMBER);
    CORE_logFunctionCall(logfunc_level,
                         logfunc_level_max,
                         0,
                         __FILE__,
                         __func__,
                         __LINE__,
                         commentstring);

    if(aoloopcontrol_var.aoconfID_looptiming == -1)
    {
        // LOOPiteration is written in cnt1 of loop timing array
        if(sprintf(imname, "aol%ld_looptiming", aoloopcontrol_var.LOOPNUMBER) <
                1)
        {
            PRINT_ERROR("sprintf wrote <1 char");
        }
        aoloopcontrol_var.aoconfID_looptiming =
            AOloopControl_IOtools_2Dloadcreate_shmim(
                imname,
                " ",
                aoloopcontrol_var.AOcontrolNBtimers,
                1,
                0.0);
    }

    GPUcnt = 2;

    GPUsetM = (int *) malloc(sizeof(int) * GPUcnt);
    for(uint32_t k = 0; k < GPUcnt; k++)
    {
        GPUsetM[k] = k + 5;
    }

    ID_CM    = image_ID(ID_CM_name);
    wfsxsize = data.image[ID_CM].md[0].size[0];
    wfsysize = data.image[ID_CM].md[0].size[1];
    NBmodes  = data.image[ID_CM].md[0].size[2];

    ID_WFSref = image_ID(ID_WFSref_name);

    sizearray    = (uint32_t *) malloc(sizeof(uint32_t) * 2);
    sizearray[0] = NBmodes;
    sizearray[1] = 1;

    create_image_ID(ID_coeff_name,
                    2,
                    sizearray,
                    _DATATYPE_FLOAT,
                    1,
                    0,
                    0,
                    &ID_coeff);
    data.image[ID_coeff].md[0].cnt0 = 0;

    create_image_ID("coefftmp",
                    2,
                    sizearray,
                    _DATATYPE_FLOAT,
                    1,
                    0,
                    0,
                    &ID_coefft);

    create_image_ID("coeff0",
                    2,
                    sizearray,
                    _DATATYPE_FLOAT,
                    1,
                    0,
                    0,
                    &IDcoeff0);
    create_2Dimage_ID("wfsim_n", wfsxsize, wfsysize, &ID_WFSim_n);

    ID_WFSim    = image_ID(ID_WFSim_name);
    ID_WFSimtot = image_ID(ID_WFSimtot_name);

    GPU_loop_MultMat_setup(2,
                           ID_CM_name,
                           "wfsim_n",
                           "coefftmp",
                           GPUcnt,
                           GPUsetM,
                           0,
                           1,
                           1,
                           0);

    totfluxave = 1.0;
    int initWFSref;
    for(;;)
    {
        if(initWFSref == 0)
        {
            printf("Computing reference\n");
            fflush(stdout);
            memcpy(data.image[ID_WFSim_n].array.F,
                   data.image[ID_WFSref].array.F,
                   sizeof(float) * wfsxsize * wfsysize);
            GPU_loop_MultMat_execute(2, &status, &GPUstatus[0], 1.0, 0.0, 0, 0);
            for(uint32_t m = 0; m < NBmodes; m++)
            {
                data.image[IDcoeff0].array.F[m] =
                    data.image[ID_coefft].array.F[m];
            }
            printf("\n");
            initWFSref = 1;
            printf("reference computed\n");
            fflush(stdout);
        }

        memcpy(data.image[ID_WFSim_n].array.F,
               data.image[ID_WFSim].array.F,
               sizeof(float) * wfsxsize * wfsysize);
        COREMOD_MEMORY_image_set_semwait(ID_WFSim_name, 0);

        GPU_loop_MultMat_execute(2, &status, &GPUstatus[0], 1.0, 0.0, 0, 0);
        totfluxave = (1.0 - alpha) * totfluxave +
                     alpha * data.image[ID_WFSimtot].array.F[0];

        data.image[ID_coeff].md[0].write = 1;
        for(uint32_t m = 0; m < NBmodes; m++)
        {
            data.image[ID_coeff].array.F[m] =
                data.image[ID_coefft].array.F[m] / totfluxave -
                data.image[IDcoeff0].array.F[m];
        }
        data.image[ID_coeff].md[0].cnt0++;
        data.image[ID_coeff].md[0].cnt1 =
            data.image[aoloopcontrol_var.aoconfID_looptiming].md[0].cnt1;
        data.image[ID_coeff].md[0].write = 0;
    }

    delete_image_ID("coeff0", DELETE_IMAGE_ERRMODE_WARNING);
    free(sizearray);

    free(GPUsetM);

    // LOG function start
    CORE_logFunctionCall(logfunc_level,
                         logfunc_level_max,
                         1,
                         __FILE__,
                         __func__,
                         __LINE__,
                         commentstring);

    return RETURN_SUCCESS;
}

